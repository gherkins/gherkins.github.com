$(function () {
    $('#term').terminal(function (command, term) {

            switch (command) {
                case 'contact':
                    term.echo("\n" +
                        "email: maxgirkens@gmail.com\n" +
                        "blog: http://nerdpress.org/author/max-girkens/\n" +
                        "github: https://github.com/gherkins " +
                        "\n");
                    break;
                case 'cv':
                    term.echo("\n" +
                        "CV: http://careers.stackoverflow.com/gherkins" +
                        "\n");
                    break;
                case 'work':

                    var output = "\n";
                    $.each(work, function () {
                        output += this.name + " - " + this.desc + " " + this.date + "\n"
                        output += this.tech + "\n";
                        output += this.link + "\n\n";
                    });

                    output += "...more coming soon\n\n";

                    term.echo(output);
                    break;
                default:
                    term.echo(command + ': command not found');
                    break;
            }

        },
        {
            prompt: '>',
            clear : false,
            greetings: function () {

                var output = '\nMAX GIRKENS\n';

                $.each(logo, function (key, row) {
                    $.each(row, function (key, char) {
                        switch (char) {
                            case 0:
                                output += "[[;;#000]  ]"
                                break;
                            default:
                                output += "[[;;#1ba9fc]  ]";
                                break;
                        }
                    });
                    output += "\n";
                });

                output += "\n" +
                    "welcome to my homepage\n" +
                    "commands: work, cv, contact\n";

                return output;
            }
        });
});