function greetings() {

    var output = '\nMax Girkens\n';

    $.each(logo, function (key, row) {
        $.each(row, function (key, char) {
            switch (char) {
                case 0:
                    output += "[[;;;blank]  ]"
                    break;
                default:
                    output += "[[;;;filled]  ]";
                    break;
            }
        });
        output += "\n";
    });

    output += "\n" +
        "commands: recentwork, cv, email, github, blog, twitter\n";

    return output;
}

function log(command) {
    try {
        _gaq.push(['_trackEvent', 'Command', command]);
    } catch (err) {
    }
}

